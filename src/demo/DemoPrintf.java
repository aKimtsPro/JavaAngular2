package demo;

public class DemoPrintf {

    public static void main(String[] args) {

        int a = 5, b = 2;

        System.out.printf("%d MOD %d = %d\n", a, b, (a%b));

        // les marqueur

        // int, short, byte, long = %d
        // char = %c
        // String = %s
        // float, double = %f
        // floating formaté : %.2f (2 chiffres après la virgule)

    }

}
